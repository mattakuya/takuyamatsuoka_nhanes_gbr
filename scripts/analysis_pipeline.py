    def compute_vif(self, max_iter: int = 5):
        """Calculates and logs/saves the Variance Inflation Factor (VIF) for all covariates in the primary model.

        For diagnostic purposes, calculated on a single light imputation rather than production MICE settings.
        Computes VIF = 1 / (1 - R^2) by regressing each covariate against the rest.
        VIF > 10 is the standard threshold for high multicollinearity.
        """
        from statsmodels.stats.outliers_influence import variance_inflation_factor
        logging.info("Computing VIF for main model covariates...")
        mdf, num_vars = self._prepare_imputation_df(self.df)

        # Single imputation for diagnostic purposes. sample_posterior=False allows early stopping.
        imp = IterativeImputer(max_iter=max_iter,
                               random_state=42, sample_posterior=False)
        idf = pd.DataFrame(imp.fit_transform(mdf[num_vars]),
                            columns=num_vars, index=mdf.index)

        # Calculate using covariates only (excluding DPQ, WTMEC2YR, and log_ratio)
        exclude = set(self.dpq_cols_clean) | {'log_ratio', 'WTMEC2YR'}
        cov_cols = [c for c in num_vars if c not in exclude]

        X = idf[cov_cols].values
        # Include intercept in VIF calculation (statsmodels convention)
        X_with_const = sm.add_constant(X, has_constant='add')
        vif_rows = []
        for i, col in enumerate(cov_cols):
            try:
                vif = variance_inflation_factor(X_with_const, i + 1)
            except Exception:
                vif = float('nan')
            vif_rows.append({'Variable': col, 'VIF': vif})
        vif_df = pd.DataFrame(vif_rows).sort_values('VIF', ascending=False)

        print("\n### VIF (Variance Inflation Factor) ###")
        print(f"{'Variable':<32} {'VIF':>10}")
        print("=" * 44)
        for _, r in vif_df.iterrows():
            v = r['VIF']
            flag = '  ⚠' if (np.isfinite(v) and v > 10) else ''
            print(f"{r['Variable']:<32} {v:>10.2f}{flag}")
        print("=" * 44)
        max_vif = vif_df['VIF'].max(skipna=True)
        if np.isfinite(max_vif) and max_vif > 10:
            logging.warning(f"Max VIF = {max_vif:.2f} > 10. Multicollinearity flagged.")
        else:
            logging.info(f"Max VIF = {max_vif:.2f} (within acceptable range).")

        self._tables.append({
            'title': 'VIF (Main Model Covariates)',
            'family': 'methodological',
            'df': vif_df.reset_index(drop=True),
        })
        return vif_df

    # =====================================================
    # Descriptive Table 1
    # =====================================================
    def run_descriptive_table1(self):
        """Generates weighted descriptive statistics by log_ratio quartiles (Table 1)."""
        logging.info("Building weighted descriptive Table 1 by log_ratio quartile...")
        df = self.df.dropna(subset=['log_ratio', 'WTMEC2YR']).copy()
        # Use rank-based qcut to handle ties robustly
        df['ratio_q'] = pd.qcut(df['log_ratio'].rank(method='first'), 4,
                                  labels=['Q1_low', 'Q2', 'Q3', 'Q4_high'])

        continuous_vars = [
            ('RIDAGEYR',        'Age (years)'),
            ('BMXBMI',          'BMI (kg/m²)'),
            ('pir',             'Poverty-Income Ratio'),
            ('log_nlr',         'log(NLR)'),
            ('alcohol_drinks',  'Drinks/day (nondrinkers as 0)'),
            ('sedentary_min',   'Sedentary min/day'),
            ('LBXSGTSI',        'GGT (U/L)'),
            ('LBXSTB',          'Total bilirubin (mg/dL)'),
        ]
        binary_vars = [
            ('is_female',         'Female %'),
            ('is_married',        'Married/partnered %'),
            ('is_ever_smoker',    'Ever smoker %'),
            ('is_ad',             'Antidepressant use %'),
            ('has_cvd',           'CVD %'),
            ('is_diabetic',       'Diabetic (HbA1c ≥ 6.5) %'),
            ('has_liver_disease', 'Self-reported liver disease %'),
        ]

        def wmean_sd(x, w):
            valid = x.notna() & w.notna()
            if not valid.any():
                return np.nan, np.nan
            xw, ww = x[valid].astype(float), w[valid].astype(float)
            mean = np.average(xw, weights=ww)
            var = np.average((xw - mean) ** 2, weights=ww)
            return mean, float(np.sqrt(var))

        def wprop(x, w):
            valid = x.notna() & w.notna()
            if not valid.any():
                return np.nan
            return np.average(x[valid].astype(float), weights=w[valid].astype(float)) * 100

        quartiles = ['Q1_low', 'Q2', 'Q3', 'Q4_high']
        rows = []

        # Sample sizes
        n_row = {'Variable': 'N (unweighted)', 'Type': 'count'}
        for q in quartiles:
            n_row[q] = str(len(df[df['ratio_q'] == q]))
        rows.append(n_row)

        for var, label in continuous_vars:
            row = {'Variable': label, 'Type': 'mean (SD)'}
            if var not in df.columns:
                for q in quartiles:
                    row[q] = '—'
                rows.append(row)
                continue
            for q in quartiles:
                sub = df[df['ratio_q'] == q]
                mean, sd = wmean_sd(sub[var], sub['WTMEC2YR'])
                row[q] = f"{mean:.2f} ({sd:.2f})" if np.isfinite(mean) else '—'
            rows.append(row)

        for var, label in binary_vars:
            row = {'Variable': label, 'Type': 'weighted %'}
            if var not in df.columns:
                for q in quartiles:
                    row[q] = '—'
                rows.append(row)
                continue
            for q in quartiles:
                sub = df[df['ratio_q'] == q]
                pct = wprop(sub[var], sub['WTMEC2YR'])
                row[q] = f"{pct:.1f}" if np.isfinite(pct) else '—'
            rows.append(row)

        table_df = pd.DataFrame(rows)
        print("\n### DESCRIPTIVE TABLE 1 BY LOG_RATIO QUARTILE ###")
        print(table_df.to_string(index=False))

        self._tables.append({
            'title': 'DESCRIPTIVE TABLE 1 BY LOG_RATIO QUARTILE',
            'family': 'descriptive',
            'df': table_df,
        })

