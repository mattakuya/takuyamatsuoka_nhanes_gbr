    @staticmethod
    def _evalue_from_or(or_value):
        """Return VanderWeele-Ding E-value for a risk/odds ratio away from 1."""
        if pd.isna(or_value) or not np.isfinite(or_value) or or_value <= 0:
            return np.nan
        if np.isclose(or_value, 1.0):
            return 1.0
        rr = or_value if or_value >= 1 else 1 / or_value
        return rr + np.sqrt(rr * (rr - 1))

    def _print_evalues_for_logistic(self, title, results, family='sensitivity'):
        """Print E-values for log-odds results after exponentiating to ORs."""
        rows = []
        for r in results:
            beta = r.get('beta', np.nan)
            ci_lo = r.get('ci_lo', np.nan)
            ci_hi = r.get('ci_hi', np.nan)
            if not np.isfinite(beta):
                rows.append({
                    'Subgroup': r.get('label'), 'N': r.get('n'),
                    'OR': np.nan, 'CI_lo_OR': np.nan, 'CI_hi_OR': np.nan,
                    'E_value': np.nan, 'E_value_CI_limit': np.nan,
                })
                continue
            or_est = float(np.exp(beta))
            or_lo = float(np.exp(ci_lo)) if np.isfinite(ci_lo) else np.nan
            or_hi = float(np.exp(ci_hi)) if np.isfinite(ci_hi) else np.nan
            if np.isfinite(or_lo) and np.isfinite(or_hi) and or_lo <= 1 <= or_hi:
                ci_limit = 1.0
            elif or_est >= 1:
                ci_limit = or_lo
            else:
                ci_limit = or_hi
            rows.append({
                'Subgroup': r.get('label'), 'N': r.get('n'),
                'OR': or_est, 'CI_lo_OR': or_lo, 'CI_hi_OR': or_hi,
                'E_value': self._evalue_from_or(or_est),
                'E_value_CI_limit': self._evalue_from_or(ci_limit),
            })

        df = pd.DataFrame(rows)
        print(f"\n### {title} ###")
        print(df.to_string(index=False, float_format=lambda x: f"{x:.4f}"))
        self._tables.append({
            'title': title,
            'family': family,
            'df': df,
        })

