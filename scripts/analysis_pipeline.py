class NHANESDepressionAnalysis:
    """Integrated pipeline for analyzing the association between GGT/Bil ratio and PHQ-9 depressive symptoms."""

    # RIDRETH3: 1=Mex American, 2=Other Hispanic, 3=NH White, 4=NH Black, 6=NH Asian, 7=Other/Multi
    RACE_MAP = {1: 'MexAm', 2: 'OtherHisp', 3: 'NHWhite', 4: 'NHBlack',
                6: 'NHAsian', 7: 'OtherMulti'}
    # RIDRETH1 is used in older cycles and does not separate NH Asian.
    RACE1_MAP = {1: 'MexAm', 2: 'OtherHisp', 3: 'NHWhite', 4: 'NHBlack',
                 5: 'OtherMulti'}

    # Reference order for race dummies (NHWhite as reference per NHANES convention)
    RACE_ORDER = ['NHWhite', 'NHBlack', 'MexAm', 'OtherHisp', 'NHAsian', 'OtherMulti']

    # Pass through design variables. WTMEC2YR is included as an auxiliary variable in num_vars.
    DESIGN_VARS = ('SDMVPSU_c', 'SDMVSTRA_c')

    def __init__(self, dataset_dir="dataset", n_jobs=None, imputer_max_iter=50):
        """
        Parameters
        ----------
        dataset_dir : str
            NHANES data directory.
        n_jobs : int, optional
            Number of parallel jobs for MICE imputation loop. If None (default) or -1,
            resolves dynamically: uses cpu_count - 2 for high-end systems (>=60GB RAM),
            and caps at 4 for smaller systems to prevent memory/thermal peaks.
            Uses joblib if not 1. Automatically falls back to 1 if joblib is not installed.
        imputer_max_iter : int
            Number of iterations for IterativeImputer. 50 is recommended for production.
        """
        self.loader = NHANESLoader(dataset_dir)
        self.df = None
        self.dpq_cols = None
        self.dpq_cols_clean = None
        self._tables = []
        
        # Determine number of parallel jobs dynamically if None or -1
        if n_jobs is None or n_jobs == -1:
            env_jobs = os.getenv("NHANES_JOBS")
            if env_jobs is not None:
                resolved_jobs = int(env_jobs)
            else:
                cpu_count = os.cpu_count() or 1
                try:
                    mem_bytes = os.sysconf('SC_PAGE_SIZE') * os.sysconf('SC_PHYS_PAGES')
                    mem_gb = mem_bytes / (1024 ** 3)
                except Exception:
                    mem_gb = 8.0 # safe fallback
                
                if mem_gb >= 60.0:
                    # High-performance workstation / server: use all available cores for maximum performance
                    resolved_jobs = cpu_count
                else:
                    # Consumer/constrained system: cap at 4 cores to prevent memory peaks
                    resolved_jobs = max(1, min(4, cpu_count - 1))
        else:
            resolved_jobs = n_jobs

        self.n_jobs = resolved_jobs if _HAS_JOBLIB else 1
        self.imputer_max_iter = imputer_max_iter
        if self.n_jobs != 1:
            logging.info(f"Dynamic parallelization enabled: using {self.n_jobs} parallel workers (processes).")
        else:
            logging.info("Sequential execution enabled (n_jobs=1).")
        if resolved_jobs != 1 and not _HAS_JOBLIB:
            logging.warning("joblib not installed; falling back to sequential execution.")

        # Antidepressant keywords: SSRIs/SNRIs + TCAs/MAOIs/Atypicals
        # * Note: SELEGILINE is excluded as its use is dominant for Parkinson's disease.
        #   (Although transdermal patch EMSAM exists for depression, it is highly limited in NHANES age range)
        self.ad_keywords = [
            # SSRI
            'SERTRALINE', 'FLUOXETINE', 'ESCITALOPRAM', 'CITALOPRAM',
            'PAROXETINE', 'FLUVOXAMINE', 'VILAZODONE', 'VORTIOXETINE',
            # SNRI
            'VENLAFAXINE', 'DESVENLAFAXINE', 'DULOXETINE',
            'LEVOMILNACIPRAN', 'MILNACIPRAN',
            # Atypical
            'BUPROPION', 'MIRTAZAPINE', 'TRAZODONE', 'NEFAZODONE',
            # TCA
            'AMITRIPTYLINE', 'NORTRIPTYLINE', 'IMIPRAMINE', 'DESIPRAMINE',
            'DOXEPIN', 'CLOMIPRAMINE', 'PROTRIPTYLINE', 'TRIMIPRAMINE',
            'AMOXAPINE', 'MAPROTILINE',
            # MAOI (selegiline excluded; see note above)
            'PHENELZINE', 'TRANYLCYPROMINE', 'ISOCARBOXAZID',
        ]

        # Statins
        self.statin_keywords = [
            'ATORVASTATIN', 'SIMVASTATIN', 'ROSUVASTATIN',
            'PRAVASTATIN', 'LOVASTATIN', 'FLUVASTATIN', 'PITAVASTATIN',
        ]

        # Antipsychotics (typical and atypical)
        self.ap_keywords = [
            # Atypical
            'RISPERIDONE', 'OLANZAPINE', 'QUETIAPINE', 'ARIPIPRAZOLE',
            'ZIPRASIDONE', 'PALIPERIDONE', 'LURASIDONE', 'ASENAPINE',
            'BREXPIPRAZOLE', 'CARIPRAZINE', 'ILOPERIDONE', 'CLOZAPINE',
            # Typical
            'HALOPERIDOL', 'CHLORPROMAZINE', 'FLUPHENAZINE', 'PERPHENAZINE',
            'TRIFLUOPERAZINE', 'THIOTHIXENE', 'LOXAPINE', 'MOLINDONE',
            'PIMOZIDE', 'THIORIDAZINE',
        ]

        # Benzodiazepines
        self.bz_keywords = [
            'DIAZEPAM', 'LORAZEPAM', 'ALPRAZOLAM', 'CLONAZEPAM',
            'TEMAZEPAM', 'OXAZEPAM', 'CHLORDIAZEPOXIDE', 'CLORAZEPATE',
            'TRIAZOLAM', 'FLURAZEPAM', 'ESTAZOLAM', 'QUAZEPAM', 'MIDAZOLAM',
        ]

        # Mood stabilizers (primary bipolar use): lithium, valproate (divalproex), lamotrigine
        self.ms_keywords = [
            'LITHIUM',
            'VALPROIC', 'VALPROATE', 'DIVALPROEX',
            'LAMOTRIGINE',
        ]

        # Antiepileptics (primary epilepsy use, excluding mood stabilizers)
        self.aed_keywords = [
            'PHENYTOIN', 'CARBAMAZEPINE', 'OXCARBAZEPINE',
            'LEVETIRACETAM', 'TOPIRAMATE',
            'GABAPENTIN', 'PREGABALIN',
            'PHENOBARBITAL', 'PRIMIDONE',
            'ZONISAMIDE', 'LACOSAMIDE',
        ]

    # =====================================================
    # Loading
    # =====================================================
    def _load_cycle_files(self, files):
        """Merges files within a cycle via outer join on SEQN. Missing files are skipped."""
        dfs = []
        for f in files:
            try:
                dfs.append(self.loader.load_file(f))
            except FileNotFoundError:
                logging.debug(f"  {f} not found, skipping.")
        if not dfs:
            raise FileNotFoundError("No files could be loaded for this cycle.")
        result = dfs[0]
        for df in dfs[1:]:
            result = pd.merge(result, df, on='SEQN', how='outer', suffixes=('', '_dup'))
            dup_cols = [c for c in result.columns if c.endswith('_dup')]
            if dup_cols:
                result.drop(columns=dup_cols, inplace=True)
        return result

    def load_multicycle_data(self, cycles=('J',)):
        """
        Loads one or more NHANES 2-year cycles and merges them.
        - Scales WTMEC2YR by dividing by the number of cycles (NHANES recommendation).
        - Creates composite IDs with cycle suffixes for SEQN, SDMVPSU, and SDMVSTRA.
        - hs-CRP (LBXHSCRP) is only measured in cycle I onward. Standard CRP in older cycles
          is from a different assay/unit (mg/dL) and is left as NaN rather than proxy-imputed.
          CRP-related sensitivity analysis is restricted to cycles I/J.
        """
        file_templates = [
            "DEMO_{c}.xpt", "BIOPRO_{c}.xpt", "DPQ_{c}.xpt", "BMX_{c}.xpt",
            "ALQ_{c}.xpt", "SMQ_{c}.xpt",
            "HSCRP_{c}.XPT",  # cycle I onward
            "CRP_{c}.xpt",    # cycle H fallback (standard CRP, different assay)
            "PAQ_{c}.XPT",
            "GHB_{c}.xpt", "GLU_{c}.xpt", "DIQ_{c}.xpt",
            "DR1TOT_{c}.XPT", "MCQ_{c}.xpt",
            "CBC_{c}.XPT",
            # Cotinine: required for OBS smoking component (Hernández-Ruiz 2019)
            # User has I/J; cycle H will be skipped silently, and OBS will be NaN for H.
            "COT_{c}.XPT",
            # Sleep duration (SLD012: weekday/workday sleep hours)
            "SLQ_{c}.xpt",
        ]
        loaded = []
        for cycle in cycles:
            logging.info(f"Loading cycle {cycle}...")
            files = [t.replace('{c}', cycle) for t in file_templates]
            try:
                df = self._load_cycle_files(files)
            except FileNotFoundError as e:
                logging.warning(f"Skipping cycle {cycle}: {e}")
                continue

            # hs-CRP (LBXHSCRP) is only measured in cycle I onward. Standard CRP
            # in older cycles has a different assay and unit (mg/dL vs mg/L),
            # so proxy substitution is avoided.
            # Cycles missing LBXHSCRP are left as NaN, and CRP-related sensitivity
            # analysis is run on cycles I/J only.
            if 'LBXHSCRP' not in df.columns:
                logging.info(
                    f"  Cycle {cycle}: LBXHSCRP not measured; "
                    f"log_crp will be NaN (used only in I/J sensitivity).")
                df['LBXHSCRP'] = np.nan

            # Medication flags: Antidepressants (AD) / statins / antipsychotics (AP) /
            # benzodiazepines (BZ) / mood stabilizers (MS) / antiepileptics (AED).
            # Each class is identified via keyword matching. A single drug is intended to
            # belong to only one category, though overlap remains theoretically possible.
            med_flag_specs = [
                ('is_ad',     self.ad_keywords),
                ('is_statin', self.statin_keywords),
                ('is_ap',     self.ap_keywords),
                ('is_bz',     self.bz_keywords),
                ('is_ms',     self.ms_keywords),
                ('is_aed',    self.aed_keywords),
            ]
            try:
                rx_df = self.loader.load_file(f"RXQ_RX_{cycle}.xpt")
                for flag_name, kw_list in med_flag_specs:
                    rx_df[flag_name] = rx_df['RXDDRUG'].str.contains(
                        '|'.join(kw_list), case=False, na=False)
                merged_flags = rx_df.groupby('SEQN')[
                    [n for n, _ in med_flag_specs]
                ].max().astype(int)
                df = df.merge(merged_flags, on='SEQN', how='left')
                for flag_name, _ in med_flag_specs:
                    df[flag_name] = df[flag_name].fillna(0).astype(float)
            except FileNotFoundError:
                logging.warning(f"RXQ_RX_{cycle}.xpt not found; setting all med flags=0")
                for flag_name, _ in med_flag_specs:
                    df[flag_name] = 0.0

            # Composite IDs (to avoid duplicates across cycles)
            suffix = f'_{cycle}'
            df['SEQN'] = df['SEQN'].astype(str) + suffix
            if 'SDMVSTRA' in df.columns:
                df['SDMVSTRA_c'] = df['SDMVSTRA'].astype('Int64').astype(str) + suffix
            if 'SDMVPSU' in df.columns and 'SDMVSTRA' in df.columns:
                df['SDMVPSU_c'] = (df['SDMVSTRA'].astype('Int64').astype(str) + '_'
                                   + df['SDMVPSU'].astype('Int64').astype(str) + suffix)
            df['cycle'] = cycle
            loaded.append(df)
            logging.info(f"  Cycle {cycle}: {len(df)} records")

        if not loaded:
            raise RuntimeError("No cycles could be loaded. Please check the file path configurations.")

        combined = pd.concat(loaded, ignore_index=True)
        # NHANES official guidelines: Divide 2-year weights by the number of cycles when combining cycles
        combined['WTMEC2YR'] = combined['WTMEC2YR'] / len(loaded)
        self.df = combined
        logging.info(
            f"Multi-cycle load complete: {len(self.df)} records from {len(loaded)} cycles.")

    # =====================================================
    # Preprocessing
    # =====================================================
    def preprocess(self):
        logging.info("Starting variable construction and cleaning...")
        df = self.df.copy()

        # 1. Restrict to age >= 18 (eligible for PHQ-9)
        before_n = len(df)
        df = df[df['RIDAGEYR'] >= 18].copy()
        logging.info(f"Age filter (≥18): {before_n} → {len(df)}")

        # 2. PHQ-9 individual items: Retain only valid responses (0-3). Total score calculated after MICE.
        self.dpq_cols = [f"DPQ{str(i).zfill(3)}" for i in range(10, 100, 10)]
        self.dpq_cols_clean = list(self.dpq_cols)
        for col in self.dpq_cols:
            df[col] = df[col].where(df[col].isin([0, 1, 2, 3]))

        # 3. Exposure: Replace values <= 0 with NaN (limit of detection or anomaly) before log transformation.
        for col in ['LBXSGTSI', 'LBXSTB', 'LBXSATSI', 'LBXSASSI']:
            if col in df.columns:
                df[col] = df[col].where(df[col] > 0)
        df['log_ratio'] = np.log(df['LBXSGTSI']) - np.log(df['LBXSTB'])
        df['log_ggt']   = np.log(df['LBXSGTSI'])
        df['log_bil']   = np.log(df['LBXSTB'])
        df['log_alt']   = np.log(df['LBXSATSI'])
        df['log_ast']   = np.log(df['LBXSASSI'])

        # 4. Diabetes: OR logic based on 3 ADA criteria
        #   Positive (->1): DIQ010==1 (doctor diagnosis) or HbA1c >= 6.5% or fasting glucose >= 126 mg/dL
        #   Negative (->0): No positive indicators AND at least one component observed
        #   Missing (->NaN): All components NaN or refused/don't know only
        _diq_pos  = (df['DIQ010'] == 1)           if 'DIQ010' in df.columns else pd.Series(False, index=df.index)
        _diq_obs  = df['DIQ010'].isin([1, 2, 3])  if 'DIQ010' in df.columns else pd.Series(False, index=df.index)
        _hba1c_pos = (df['LBXGH'].notna() & (df['LBXGH'] >= 6.5)) if 'LBXGH' in df.columns else pd.Series(False, index=df.index)
        _hba1c_obs = df['LBXGH'].notna()    if 'LBXGH' in df.columns else pd.Series(False, index=df.index)
        _fpg_pos  = (df['LBXGLU'].notna() & (df['LBXGLU'] >= 126)) if 'LBXGLU' in df.columns else pd.Series(False, index=df.index)
        _fpg_obs  = df['LBXGLU'].notna()          if 'LBXGLU' in df.columns else pd.Series(False, index=df.index)
        _any_pos  = _diq_pos | _hba1c_pos | _fpg_pos
        _any_obs  = _diq_obs | _hba1c_obs | _fpg_obs
        df['is_diabetic'] = np.where(_any_pos, 1.0, np.where(_any_obs, 0.0, np.nan))

        # 5. CVD: Convert 7/9 to NaN first. NaN if all components missing, 1 if any is 1, else 0.
        # MCQ160B=Heart Failure, C=Coronary Heart Disease, D=Angina, E=Myocardial Infarction, F=Stroke
        cv_cols = ['MCQ160B', 'MCQ160C', 'MCQ160D', 'MCQ160E', 'MCQ160F']
        cv_clean = df[cv_cols].replace({7: np.nan, 9: np.nan})
        any_pos = (cv_clean == 1).any(axis=1)
        any_obs = cv_clean.notna().any(axis=1)
        df['has_cvd'] = np.where(any_pos, 1.0, np.where(any_obs, 0.0, np.nan))

        # 6. Self-reported liver disease / Inflammation / Normal liver enzyme flag / Renal function (creatinine)
        df['has_liver_disease'] = df['MCQ160L'].map({1: 1, 2: 0})
        df['log_crp'] = np.log(df['LBXHSCRP'] + 0.01)

        # CBC-derived inflammatory indices. Prefer absolute counts when present.
        # LBDNENO/LBDLYMNO are in 1000 cells/uL; percent ratios are a fallback.
        if {'LBDNENO', 'LBDLYMNO'}.issubset(df.columns):
            neut = df['LBDNENO'].where(df['LBDNENO'] > 0)
            lymph = df['LBDLYMNO'].where(df['LBDLYMNO'] > 0)
        elif {'LBXNEPCT', 'LBXLYPCT'}.issubset(df.columns):
            neut = df['LBXNEPCT'].where(df['LBXNEPCT'] > 0)
            lymph = df['LBXLYPCT'].where(df['LBXLYPCT'] > 0)
        else:
            neut = pd.Series(np.nan, index=df.index)
            lymph = pd.Series(np.nan, index=df.index)
        df['nlr'] = neut / lymph
        df['log_nlr'] = np.log(df['nlr'].where(df['nlr'] > 0))
        if 'LBXPLTSI' in df.columns:
            platelets = df['LBXPLTSI'].where(df['LBXPLTSI'] > 0)
            df['plr'] = platelets / lymph
            df['sii'] = platelets * neut / lymph
            df['log_sii'] = np.log(df['sii'].where(df['sii'] > 0))
        else:
            df['plr'] = np.nan
            df['sii'] = np.nan
            df['log_sii'] = np.nan

        # Log-transform serum creatinine (LBXSCR, mg/dL). Uses raw creatinine
        # rather than eGFR as covariates adjust for age and sex (informationally equivalent).
        if 'LBXSCR' in df.columns:
            df['log_creat'] = np.log(df['LBXSCR'].where(df['LBXSCR'] > 0))
        else:
            df['log_creat'] = np.nan

        alt_ast_known = df['LBXSATSI'].notna() & df['LBXSASSI'].notna()
        liver_safe_mask = (df['LBXSATSI'] < 40) & (df['LBXSASSI'] < 40)
        df['is_liver_safe'] = np.where(alt_ast_known,
                                        liver_safe_mask.astype(float),
                                        np.nan)

        # 7. Demographics (retain NaN for race, dropna prior to dummy coding)
        df['is_female']  = df['RIAGENDR'].map({1: 0, 2: 1})
        race3 = (df['RIDRETH3'].map(self.RACE_MAP)
                 if 'RIDRETH3' in df.columns else pd.Series(np.nan, index=df.index))
        race1 = (df['RIDRETH1'].map(self.RACE1_MAP)
                 if 'RIDRETH1' in df.columns else pd.Series(np.nan, index=df.index))
        df['race']       = race3.combine_first(race1)
        df['pir']        = df['INDFMPIR']
        df['is_married'] = df['DMDMARTL'].map({1: 1, 2: 0, 3: 0, 4: 0, 5: 0, 6: 1})

        # Education (3 categories): "more_HS" is used as the reference.
        #   DMDEDUC2: 1=<9th grade, 2=9-11th, 3=HS/GED, 4=Some college, 5=College+,
        #             7=Refused, 9=Don't know
        _edu_map = {
            1: 'less_HS', 2: 'less_HS',
            3: 'HS_grad',
            4: 'more_HS', 5: 'more_HS',
        }
        if 'DMDEDUC2' in df.columns:
            df['education_cat'] = df['DMDEDUC2'].map(_edu_map)
        else:
            df['education_cat'] = np.nan

        # 8. Lifestyle
        df['total_calories'] = df['DR1TKCAL']
        df['alcohol_drinks'] = df['ALQ130'].where(~df['ALQ130'].isin([777, 999]))
        # Fill confirmed non-drinkers with 0 (imputing NaN via MICE would overestimate alcohol intake)
        # Cycle I: ALQ101==2 (less than 12 drinks/year) and ALQ130 is NaN -> No drinking
        # Cycle J: ALQ111==2 (lifetime non-drinker) or ALQ121==0 (0 drinks in past 12 months)
        nd_mask = pd.Series(False, index=df.index)
        if 'ALQ101' in df.columns:
            nd_mask |= (df['ALQ101'] == 2) & df['ALQ130'].isna()
        if 'ALQ111' in df.columns:
            nd_mask |= (df['ALQ111'] == 2)
        if 'ALQ121' in df.columns:
            nd_mask |= (df['ALQ121'] == 0)
        df.loc[nd_mask, 'alcohol_drinks'] = 0.0
        logging.info(f"  Set alcohol_drinks=0 for {int(nd_mask.sum())} confirmed non-drinkers.")
        # is_ever_smoker is a primary model covariate; log_cot is used in extended sensitivity analyses.
        df['is_ever_smoker'] = df['SMQ020'].map({1: 1, 2: 0})
        df['sedentary_min']  = df['PAD680'].where(df['PAD680'] <= 1440)

        # 8a. Alcohol stratification based on NIAAA criteria (derived from weekly intake)
        #   Cycle I: ALQ120Q (frequency numeric) * ALQ120U (unit: 1=week, 2=month, 3=year)
        #   Cycle J: ALQ121 (category 0-10) -> mapped to weekly drinking days
        #   Heavy drinking: Males > 14 drinks/week, Females > 7 drinks/week
        ALQ121_TO_DAYS = {
            0: 0.0, 1: 7.0, 2: 5.0, 3: 3.5, 4: 2.0, 5: 1.0,
            6: 2.5 / 4.345, 7: 1.0 / 4.345,
            8: 9.0 / 52, 9: 4.5 / 52, 10: 1.5 / 52,
        }
        days_pw = pd.Series(np.nan, index=df.index, dtype=float)
        # Cycle I calculations
        if 'ALQ120Q' in df.columns and 'ALQ120U' in df.columns:
            q = df['ALQ120Q'].where(~df['ALQ120Q'].isin([777, 999]))
            u = df['ALQ120U'].where(~df['ALQ120U'].isin([7, 9]))
            cycle_i_days = pd.Series(np.nan, index=df.index, dtype=float)
            cycle_i_days = cycle_i_days.mask(u == 1, q)
            cycle_i_days = cycle_i_days.mask(u == 2, q / 4.345)
            cycle_i_days = cycle_i_days.mask(u == 3, q / 52)
            days_pw = days_pw.fillna(cycle_i_days)
        # Cycle J calculations (do not overwrite if already filled by Cycle I)
        if 'ALQ121' in df.columns:
            cycle_j_days = df['ALQ121'].map(ALQ121_TO_DAYS)
            days_pw = days_pw.fillna(cycle_j_days)
        # Confirmed non-drinkers get 0 days/week
        days_pw.loc[nd_mask] = 0.0
        df['drinking_days_per_week'] = days_pw
        df['drinks_per_week'] = days_pw * df['alcohol_drinks']

        # NIAAA categories (Heavy: Females >7 / Males >14 drinks/week)
        wpw = df['drinks_per_week']
        sex_f = df['is_female']
        cutoff = pd.Series(np.where(sex_f == 1, 7.0, 14.0), index=df.index)
        cat = pd.Series('lowrisk', index=df.index, dtype=object)
        cat = cat.mask(wpw == 0, 'nondrinker')
        cat = cat.mask(wpw > cutoff, 'heavy')
        cat = cat.mask(wpw.isna() | sex_f.isna(), np.nan)
        df['alcohol_cat'] = cat

        # 8b. Sleep duration (weekday/workday, SLD012, hours)
        if 'SLD012' in df.columns:
            df['sleep_hours'] = df['SLD012'].where(
                (df['SLD012'] >= 1) & (df['SLD012'] <= 24))
        else:
            df['sleep_hours'] = np.nan

        # 9. Dietary recall reliability flag (set DR1T* to NaN if DR1DRSTZ != 1)
        # NHANES DR1DRSTZ: 1=Reliable & met minimum, 4=Reported but missed meal, etc.
        # OBS and CDAI are constructed only for individuals with reliable dietary recall.
        if 'DR1DRSTZ' in df.columns:
            unreliable_mask = df['DR1DRSTZ'] != 1
            dr1_cols = [c for c in df.columns if c.startswith('DR1T')]
            if dr1_cols:
                n_masked = unreliable_mask.sum()
                df.loc[unreliable_mask, dr1_cols] = np.nan
                logging.info(
                    f"  Masked DR1T* for {n_masked} participants with "
                    f"unreliable/incomplete dietary recall (DR1DRSTZ != 1).")

        # 10. Serum cotinine (objective smoking exposure including secondhand smoke, log-transformed)
        # Simply calculate log(LBXCOT + 0.01); values below LOD are transformed directly
        # (avoiding post-hoc substitution like LOD/sqrt(2) which introduces bias).
        # Missing values are imputed via MICE, preserving the observed continuous distribution.
        if 'LBXCOT' in df.columns:
            df['cotinine']  = df['LBXCOT']
            df['log_cot']   = np.log(df['LBXCOT'].where(df['LBXCOT'] >= 0) + 0.01)
        else:
            df['cotinine'] = np.nan
            df['log_cot']  = np.nan

        # 11. Physical activity (MET-minutes/week) calculated from PAQ
        # NHANES PAQ (H/I/J): vigorous work/recreation = 8 METs,
        #   moderate work/recreation / walk-bike-transport = 4 METs
        #   Sum of days_per_week * min_per_day * MET
        def _safe_num(s):
            return pd.to_numeric(s, errors='coerce')

        paq_blocks = [
            ('PAQ605', 'PAQ610', 'PAD615', 8.0),   # vigorous work
            ('PAQ620', 'PAQ625', 'PAD630', 4.0),   # moderate work
            ('PAQ635', 'PAQ640', 'PAD645', 4.0),   # walk/bicycle transport
            ('PAQ650', 'PAQ655', 'PAD660', 8.0),   # vigorous recreation
            ('PAQ665', 'PAQ670', 'PAD675', 4.0),   # moderate recreation
        ]
        met_min = pd.Series(0.0, index=df.index)
        any_component = pd.Series(False, index=df.index)
        # Participants who answered "Yes" but have missing/invalid days/minutes
        # are set to NaN to let MICE impute, preventing downward bias by assuming 0
        invalid_response = pd.Series(False, index=df.index)
        for yn, days, mins, met in paq_blocks:
            if not all(c in df.columns for c in (yn, days, mins)):
                continue
            yn_s    = _safe_num(df[yn])
            days_s  = _safe_num(df[days]).where(lambda s: s.between(1, 7))
            mins_s  = _safe_num(df[mins]).where(lambda s: s.between(1, 1440))
            # yn==2 (No) -> 0 MET-min, yn==1 (Yes) -> days*mins*MET
            no_mask  = yn_s == 2
            yes_mask = yn_s == 1
            invalid_response = invalid_response | (
                yes_mask & (days_s.isna() | mins_s.isna()))
            block_value = days_s * mins_s * met
            met_min = met_min.add(block_value.where(yes_mask, 0.0).fillna(0.0),
                                    fill_value=0.0)
            any_component = any_component | yes_mask | no_mask
        df['met_min_week'] = met_min.where(any_component & ~invalid_response)

        self.df = df

        # 12. Build oxidative balance indices (CDAI / OBS / NutrientL-OBS)
        self._build_oxidative_indices()

        logging.info("Preprocessing completed successfully.")

    # =====================================================
    # Oxidative balance indices: CDAI / OBS / NutrientL-OBS
    # =====================================================
    def _build_oxidative_indices(self):
        """
        Builds CDAI (Wright 2004) and OBS (Hernández-Ruiz 2019, 20-component).

        CDAI: Sum of standardized z-scores (mean/SD of full sample) of 6 components:
          - Vitamin A (DR1TVARA), Vitamin C (DR1TVC), Vitamin E (DR1TATOC),
            Zinc (DR1TZINC), Selenium (DR1TSELE),
            and Carotenoids (alpha + beta + beta-cryptoxanthin + lutein + zeaxanthin + lycopene).

        OBS: Sum of sex-specific tertile scores (0/1/2) for 20 components (16 dietary + 4 lifestyle):
          Antioxidants (higher -> higher score; T1=0, T2=1, T3=2):
            beta-carotene, fiber, copper, B6, B12, vit C, niacin, vit E, folate (DFE),
            B2, Mg, Ca, Zn, Se, physical activity (MET-min/week).
          Pro-oxidants (higher -> lower score; T1=2, T2=1, T3=0):
            total fat, iron, alcohol (DR1TALCO g/day), BMI, cotinine.

        NutrientL-OBS: Sensitivity version using only the 16 dietary nutrients.
        Since PA, BMI, alcohol, and cotinine are directly linked to depression,
        this dietary-only version isolates nutrient effects to check for masking.
        """
        logging.info("Building oxidative indices (CDAI / OBS / NutrientL-OBS)...")
        df = self.df.copy()

        # ---- CDAI Components ----
        cdai_atoms = {
            'vitA':       'DR1TVARA',
            'vitC':       'DR1TVC',
            'vitE':       'DR1TATOC',
            'zinc':       'DR1TZINC',
            'selenium':   'DR1TSELE',
        }
        # Carotenoid composition (alpha-car + beta-car + beta-cryptoxanthin + lutein+zeaxanthin + lycopene)
        carot_cols = [c for c in ['DR1TACAR', 'DR1TBCAR', 'DR1TCRYP',
                                    'DR1TLZ', 'DR1TLYCO'] if c in df.columns]
        if carot_cols:
            df['carotenoids'] = df[carot_cols].sum(axis=1, min_count=1)
        else:
            df['carotenoids'] = np.nan
        cdai_atoms['carotenoids'] = 'carotenoids'

        def _zscore(col):
            if col not in df.columns:
                return pd.Series(np.nan, index=df.index)
            s = df[col].astype(float)
            mu, sd = s.mean(skipna=True), s.std(skipna=True)
            if not sd or not np.isfinite(sd) or sd == 0:
                return pd.Series(np.nan, index=df.index)
            return (s - mu) / sd

        cdai_z = pd.DataFrame({name: _zscore(c) for name, c in cdai_atoms.items()})
        # Calculate CDAI only for individuals with all 6 components (partial missingness -> NaN)
        df['cdai'] = cdai_z.sum(axis=1, min_count=len(cdai_atoms))

        # ---- OBS Component Table ----
        # (name, column, direction)  direction: +1=antioxidant, -1=prooxidant
        obs_components = [
            # Dietary antioxidants (9 nutrients) — higher = better
            ('bcar',      'DR1TBCAR', +1),
            ('fiber',     'DR1TFIBE', +1),
            ('copper',    'DR1TCOPP', +1),
            ('b6',        'DR1TVB6',  +1),
            ('b12',       'DR1TVB12', +1),
            ('vitC',      'DR1TVC',   +1),
            ('niacin',    'DR1TNIAC', +1),
            ('vitE',      'DR1TATOC', +1),
            ('folate',    'DR1TFDFE', +1),
            # Dietary antioxidants (5 more) — higher = better
            ('b2',        'DR1TVB2',  +1),
            ('mg',        'DR1TMAGN', +1),
            ('ca',        'DR1TCALC', +1),
            ('zn',        'DR1TZINC', +1),
            ('se',        'DR1TSELE', +1),
            # Dietary pro-oxidants (3) — higher = worse
            ('fat',       'DR1TTFAT', -1),
            ('iron',      'DR1TIRON', -1),
            ('alcohol_g', 'DR1TALCO', -1),
            # Lifestyle: PA antioxidant (+), BMI / cotinine pro-oxidant (-)
            ('pa',        'met_min_week', +1),
            ('bmi_ox',    'BMXBMI',       -1),
            ('cot',       'cotinine',     -1),
        ]
        # Nutrient (L) subset: 16 dietary nutrients only
        dietary_names = {'bcar', 'fiber', 'copper', 'b6', 'b12', 'vitC',
                          'niacin', 'vitE', 'folate', 'b2', 'mg', 'ca',
                          'zn', 'se', 'fat', 'iron'}

        # Calculate tertiles separately by sex
        def _sex_tertile_score(series, direction, sex_mask):
            """Returns tertile score (0/1/2) within sex subgroup."""
            out = pd.Series(np.nan, index=series.index, dtype=float)
            sub = series[sex_mask]
            valid = sub.notna()
            if valid.sum() < 30:
                return out
            # pd.qcut with duplicates='drop' handles ties / degenerate dists
            try:
                tert = pd.qcut(sub[valid].rank(method='first'), 3,
                                labels=[0, 1, 2])
                tert = tert.astype(float)
            except ValueError:
                return out
            if direction == -1:
                tert = 2 - tert
            out.loc[sub.index[valid]] = tert.values
            return out

        male_mask   = df['is_female'] == 0
        female_mask = df['is_female'] == 1

        obs_scores = {}
        for name, col, sign in obs_components:
            if col not in df.columns:
                obs_scores[name] = pd.Series(np.nan, index=df.index)
                continue
            male_s   = _sex_tertile_score(df[col], sign, male_mask)
            female_s = _sex_tertile_score(df[col], sign, female_mask)
            combined = male_s.combine_first(female_s)
            obs_scores[name] = combined

        obs_df = pd.DataFrame(obs_scores)

        # Full OBS: Scored only for individuals with all 20 components
        full_cols = [n for (n, _, _) in obs_components]
        df['obs'] = obs_df[full_cols].sum(axis=1, min_count=len(full_cols))

        # NutrientL-OBS: 16 dietary nutrients only
        nut_cols = [n for n in full_cols if n in dietary_names]
        df['obs_nutrient'] = obs_df[nut_cols].sum(axis=1, min_count=len(nut_cols))

        n_cdai = int(df['cdai'].notna().sum())
        n_obs  = int(df['obs'].notna().sum())
        n_obs_n = int(df['obs_nutrient'].notna().sum())
        logging.info(
            f"  Oxidative indices available: "
            f"CDAI n={n_cdai}, OBS n={n_obs}, NutrientL-OBS n={n_obs_n}")

        self.df = df

    # =====================================================
    # MICE Utilities
    # =====================================================
    # Reference category for education (fixed to "more_HS")
    EDUCATION_ORDER = ['more_HS', 'HS_grad', 'less_HS', 'education_missing']

    def _prepare_imputation_df(self, df_subset, include_race=True, include_sex=True,
                                exposure='log_ratio', extra_vars=None):
        """
        Returns the data frame configured for MICE.
        - Includes WTMEC2YR as an auxiliary imputation variable (excluded from regression formulas).
        - Adds has_liver_disease to the imputation variables.
        - SDMVPSU_c and SDMVSTRA_c are passed through without imputation.
        - Drops rows with missing race or education (prevents implicit reference collapse in get_dummies).
        - Fixes NHWhite as race reference and more_HS as education reference.
        - Excludes is_female completely when include_sex=False (for sex-stratified analysis).
        - extra_vars: List of additional variables to include in both imputation and regression (e.g., joint models).
        """
        # Primary models are built from a biomarker perspective:
        #   - Adjusting for variables that reflect sources of oxidative stress (smoking, renal function, sleep)
        #     in the primary model causes mediator over-adjustment, obscuring GBR's value as a screening proxy.
        #     These candidate mediators are evaluated in extended sensitivity models.
        #   - Primary model additions: Education (socioeconomic confounder), statins (cardiovascular therapy,
        #     not a proxy for psychiatric treatment).
        #   - Smoking is coarsely adjusted for via binary ever-smoker status (is_ever_smoker).
        final_vars = [
            exposure, 'RIDAGEYR', 'BMXBMI',
            'pir', 'is_married', 'total_calories', 'alcohol_drinks',
            'is_ever_smoker', 'log_nlr', 'sedentary_min',
            'is_diabetic', 'has_cvd',
            'is_ad', 'is_statin', 'has_liver_disease',
            'WTMEC2YR',  # Auxiliary variable in imputation model; not included in regression formulas
        ] + self.dpq_cols
        if include_sex:
            final_vars.insert(2, 'is_female')
        if include_race:
            final_vars = ['race'] + final_vars
        # Education has 3 categories. Drop NaN before dummy coding to place near the beginning with race.
        final_vars = ['education_cat'] + final_vars
        if extra_vars:
            final_vars = final_vars + [v for v in extra_vars
                                         if v not in final_vars
                                         and v in df_subset.columns]

        # Extract variables actually present in df_subset (avoids breaking on older subsets)
        present_vars = [v for v in final_vars if v in df_subset.columns]

        design_present = [c for c in self.DESIGN_VARS if c in df_subset.columns]
        # Drop rows missing WTMEC2YR (equivalent to zero weight)
        mdf = df_subset.dropna(subset=['WTMEC2YR'])[present_vars + design_present].copy()

        # Retain education NaN as a "missing" category, then dummy code with more_HS as reference
        if 'education_cat' in mdf.columns:
            missing_edu = int(mdf['education_cat'].isna().sum())
            if missing_edu > 0:
                logging.info(
                    f"  Assigning {missing_edu} rows with missing education "
                    f"to education_missing.")
                mdf['education_cat'] = mdf['education_cat'].fillna(
                    'education_missing')
            present_edu = [e for e in self.EDUCATION_ORDER
                            if e in mdf['education_cat'].unique()]
            mdf['education_cat'] = pd.Categorical(
                mdf['education_cat'], categories=present_edu, ordered=False)
            mdf = pd.get_dummies(mdf, columns=['education_cat'],
                                  drop_first=True, dtype=float)

        if include_race:
            # Drop rows with missing race (prevents silent reference collapse)
            before = len(mdf)
            mdf = mdf.dropna(subset=['race']).copy()
            dropped = before - len(mdf)
            if dropped > 0:
                logging.info(f"  Dropped {dropped} rows with missing race.")

            # Fix NHWhite as reference
            present_cats = [r for r in self.RACE_ORDER if r in mdf['race'].unique()]
            mdf['race'] = pd.Categorical(
                mdf['race'], categories=present_cats, ordered=False)
            mdf = pd.get_dummies(mdf, columns=['race'], drop_first=True, dtype=float)

        mdf.columns = [c.replace('.', '_').replace(' ', '_') for c in mdf.columns]

        # Imputation targets are variables other than design columns (WTMEC2YR included as auxiliary)
        num_vars = [c for c in mdf.columns if c not in self.DESIGN_VARS]
        return mdf, num_vars

    @staticmethod
    def _apply_rubins(coefs, variances, m, nu_com=None):
        """
        Rubin's Rules + Barnard-Rubin (1999) complete degrees-of-freedom correction.
        nu_com: Complete-data degrees of freedom. n_psu - n_strata is recommended for NHANES.
        """
        theta_bar = float(np.mean(coefs))
        u_bar = float(np.mean(variances))
        b = float(np.var(coefs, ddof=1)) if len(coefs) > 1 else 0.0
        total_var = u_bar + (1 + 1 / m) * b
        se = float(np.sqrt(total_var)) if total_var > 0 else np.nan

        if b <= 0 or total_var <= 0 or not np.isfinite(total_var):
            df_r = np.inf
        else:
            gamma = (1 + 1 / m) * b / total_var
            df_old = (m - 1) / (gamma ** 2)
            if nu_com is None or not np.isfinite(nu_com):
                df_r = df_old
            else:
                df_obs = ((nu_com + 1) / (nu_com + 3)) * nu_com * (1 - gamma)
                df_r = (df_old * df_obs) / (df_old + df_obs)

        if se and np.isfinite(se) and se > 0:
            t_stat = theta_bar / se
            p = 2 * float(stats.t.sf(abs(t_stat), df=df_r))
        else:
            p = np.nan
        return theta_bar, se, p, df_r

    @staticmethod
    def _rcs_basis(x, knots):
        """
        Harrell RCS basis. Shape (n, k-1): Col 0 = linear term, Col 1.. = non-linear terms (k-2 terms).
        Ensures comparable bases by using identical knots across all imputations.
        """
        x = np.asarray(x, dtype=float)
        t = np.asarray(knots, dtype=float)
        k, n = len(t), len(x)
        out = np.zeros((n, k - 1))
        out[:, 0] = x
        denom = (t[-1] - t[0]) ** 2
        def _p3(u): return np.maximum(u, 0.0) ** 3
        for j in range(k - 2):
            out[:, j + 1] = (
                _p3(x - t[j])
                - _p3(x - t[-2]) * (t[-1] - t[j]) / (t[-1] - t[-2])
                + _p3(x - t[-1]) * (t[-2] - t[j]) / (t[-1] - t[-2])
            ) / denom
        return out

    def _fit_with_cluster(self, model, idf):
        """Cluster-robust fit using SDMVPSU_c if present, otherwise falls back to standard WLS."""
        if 'SDMVPSU_c' in idf.columns and idf['SDMVPSU_c'].nunique() >= 2:
            try:
                return model.fit(cov_type='cluster',
                                  cov_kwds={'groups': idf['SDMVPSU_c']})
            except Exception as e:
                logging.warning(f"  cluster-robust fit failed ({e}); falling back to default.")
        return model.fit()

    @staticmethod
    def _strata_nu(idf):
        """
        Returns effective degrees of freedom (n_psu - n_strata) compliant with NHANES.
        Under Taylor Series Linearization, each stratum yields (PSUs - 1) df, totaling
        n_psu - n_strata. This is the standard formula in survey/SAS/Stata.
        """
        if 'SDMVSTRA_c' in idf.columns and 'SDMVPSU_c' in idf.columns:
            n_strata = idf['SDMVSTRA_c'].nunique()
            n_psu = idf['SDMVPSU_c'].nunique()
            df_resid = float(n_psu - n_strata)
            return df_resid if df_resid > 0 else None
        return None

    # =====================================================
    # MICE + Rubin Regression Core
    # =====================================================
    def _run_mice_core(self, df_subset, m, label,
                        include_race=True, include_sex=True,
                        exposure='log_ratio',
                        standardize_exposure=False,
                        extract_params=None,
                        logistic_threshold=None,
                        extra_formula_terms=None,
                        include_bmi_interaction=True,
                        include_bmi_term=True,
                        exclude_covariates=None,
                        include_vars=None,
                        rcs_knots=None,
                        return_cov_coef=False,
                        return_metrics=False,
                        exclude_exposure_term=False):
        """
        Performs MICE imputation and weighted regression M times, pooling via Rubin's Rules in R.
        """
        mdf, num_vars = self._prepare_imputation_df(
            df_subset, include_race, include_sex, exposure,
            extra_vars=include_vars)
        exp_c = f"{exposure}_c"

        # Construct BMI terms (main effect / interaction)
        if exclude_exposure_term:
            if include_bmi_term:
                formula_bmi = "bmi_c"
            else:
                formula_bmi = ""
            default_params = []
        else:
            if include_bmi_interaction and include_bmi_term:
                formula_bmi = f"{exp_c} * bmi_c"
                default_params = [exp_c, f"{exp_c}:bmi_c"]
            elif include_bmi_term:
                formula_bmi = f"{exp_c} + bmi_c"
                default_params = [exp_c]
            else:
                # Exclude BMI completely (e.g., for OBS exposure)
                formula_bmi = f"{exp_c}"
                default_params = [exp_c]

        if extract_params is None:
            extract_params = default_params

        exclude_covariates = set(exclude_covariates or [])

        # Standardize by observed SD (fixes reference scale across imputations)
        if standardize_exposure:
            sd = mdf[exposure].std(skipna=True)
            if sd and sd > 0:
                mdf[exposure] = mdf[exposure] / sd

        exp_mean = mdf[exposure].mean(skipna=True)
        bmi_mean = mdf['BMXBMI'].mean(skipna=True)

        extra_terms_set = set(extra_formula_terms or [])
        covs_excl = (set(self.dpq_cols_clean)
                     | {exposure, 'BMXBMI', 'WTMEC2YR'}
                     | exclude_covariates
                     | extra_terms_set)
        covs = [c for c in num_vars if c not in covs_excl]

        # Define RCS variables to include in num_vars if rcs_knots is specified
        rcs_cols = []
        if rcs_knots is not None:
            rcs_cols = ['rcs_lin'] + [f'rcs_nl{j+1}' for j in range(len(rcs_knots) - 2)]
            for col in rcs_cols:
                if col not in num_vars:
                    num_vars.append(col)

        def _single_imp(seed_offset):
            """Runs a single imputation, constructs necessary variables, and returns the imputed DataFrame."""
            imp = IterativeImputer(max_iter=self.imputer_max_iter,
                                    random_state=42 + seed_offset,
                                    sample_posterior=True)
            # Impute excluding RCS basis variables to prevent circular features
            impute_vars = [c for c in num_vars if c not in rcs_cols]
            idf = pd.DataFrame(imp.fit_transform(mdf[impute_vars]),
                                columns=impute_vars, index=mdf.index)

            # Restore design variables (PSU/Strata) to pre-imputed values
            for dv in self.DESIGN_VARS:
                if dv in mdf.columns:
                    idf[dv] = mdf[dv].values

            # PHQ-9: Sum items as continuous scores (no rounding).
            phq_items = idf[self.dpq_cols_clean].clip(0, 3)
            idf['phq9_score'] = phq_items.sum(axis=1)

            idf[exp_c]   = idf[exposure] - exp_mean
            idf['bmi_c'] = idf['BMXBMI'] - bmi_mean

            if rcs_knots is not None:
                knots_c = rcs_knots
                basis = self._rcs_basis(idf[exp_c].values, knots_c)
                for j, col in enumerate(rcs_cols):
                    idf[col] = basis[:, j]

            if logistic_threshold is not None:
                idf['phq9_hi'] = (idf['phq9_score'] >= logistic_threshold).astype(int)

            return idf

        # Sequential or parallel execution
        if self.n_jobs != 1 and _HAS_JOBLIB:
            logging.info(f"  [{label}] Running {m} imputations in parallel (n_jobs={self.n_jobs})")
            idf_list = Parallel(n_jobs=self.n_jobs, backend='loky')(
                delayed(_single_imp)(i) for i in range(m)
            )
        else:
            idf_list = []
            for i in range(m):
                logging.info(f"  [{label}] Imputation {i+1}/{m}...")
