# Pipeline Execution
# =========================================================
def run_full_pipeline(pipeline, m, output_dir='results', output_prefix='nhanes_results',
                      checkpoint_each_step=False):
    """Runs all analyses sequentially using a single m, saving outputs to Excel and Markdown.
    Assumes m=20 for production and m=3 for dry-runs."""
    pipeline.load_multicycle_data(cycles=('E', 'F', 'G', 'H', 'I', 'J'))
    pipeline.preprocess()

    normal_bmi = pipeline.df[(pipeline.df['BMXBMI'] >= 18.5)
                              & (pipeline.df['BMXBMI'] < 25.0)]
    no_ad = pipeline.df[pipeline.df['is_ad'] == 0]

    # Halve m for high-subgroup analyses (e.g., race-stratified specificity, OBS tertiles)
    # to lower peak memory. (m >= 10 is sufficient for estimating Rubin's between-imputation variance)
    m_heavy = max(m // 2, 5)

    def _checkpoint(label):
        """Ensure results are saved up to the last successful step in case of failure."""
        safe = re.sub(r'[^0-9A-Za-z_-]+', '_', label).strip('_')[:60]
        prefix = f"{output_prefix}_{safe}" if safe else output_prefix
        pipeline.save_outputs(output_dir=output_dir, prefix=prefix)

    def _step(fn, *args, label=None, checkpoint=None, **kwargs):
        """Run single analysis -> Save -> Free memory via GC immediately"""
        fn(*args, **kwargs)
        if checkpoint is None:
            checkpoint = checkpoint_each_step
        if checkpoint:
            _checkpoint(label or fn.__name__)
        gc.collect()

    _step(pipeline.run_descriptive_table1)
    _step(pipeline.run_descriptive_by_race)
    _step(pipeline.compute_vif)

    _step(pipeline.run_mice_analysis, m=m)
    _step(pipeline.run_bmi_stratified_analysis, m=m,
          include_full_sample=False)
    _step(pipeline.run_sex_stratified_analysis, m=m,
          include_full_sample=False)
    _step(pipeline.run_race_stratified_analysis, m=m, family='primary')
    _step(pipeline.run_race_interaction_analysis, m=m, exposure='log_ratio')
    _step(pipeline.run_race_stratified_analysis,
          m=m, df_subset=no_ad, title_suffix=" (Excl. AD Users)",
          family='sensitivity')
    _step(pipeline.run_ij_hscrp_sensitivity, m=m)

    _step(pipeline.run_alcohol_stratified_analysis, m=m)
    _step(pipeline.run_age_stratified_analysis, m=m)
    _step(pipeline.run_crp_stratified_analysis, m=m)
    _step(pipeline.run_cotinine_stratified_analysis, m=m)

    for exp in ('log_ratio', 'log_ggt'):
        _step(pipeline.run_ad_interaction_analysis, m=m, exposure=exp)
        _step(pipeline.run_ad_interaction_analysis,
              m=m, df_subset=normal_bmi, exposure=exp,
              title_suffix=" (Normal BMI)")
        _step(pipeline.run_smoking_interaction_analysis, m=m, exposure=exp)

    _step(pipeline.run_sensitivity_analysis, m=m,
          include_full_sample=False)
    _step(pipeline.run_hispanic_subanalysis, m=m)
    _step(pipeline.run_liver_safe_analysis, m=m,
          include_full_sample=False)
    _step(pipeline.run_specificity_analysis, m=m)
    _step(pipeline.run_race_specificity_analysis, m=m_heavy)
