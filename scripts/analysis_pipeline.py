class NHANESLoader:
    """
    Utility class to load and merge NHANES datasets by SEQN.
    Handles case-insensitive file extensions and cycle-specific directory paths.
    """
    CYCLE_DIRS = {
        'E': '2007-2008',
        'F': '2009-2010',
        'G': '2011-2012',
        'H': '2013-2014',
        'I': '2015-2016',
        'J': '2017-2018',
    }

    def __init__(self, dataset_dir: str = "dataset"):
        self.dataset_dir = dataset_dir

    def find_file(self, filename: str) -> str:
        """Resolves case differences and directory structures, returning the actual file path."""
        base, _ = os.path.splitext(filename)
        candidates = [filename, f"{base}.xpt", f"{base}.XPT"]

        search_dirs = [self.dataset_dir]
        cycle_match = re.search(r'_([A-Z])(?:\.xpt)?$', filename, flags=re.IGNORECASE)
        if cycle_match:
            cycle = cycle_match.group(1).upper()
            cycle_dir = self.CYCLE_DIRS.get(cycle)
            if cycle_dir:
                search_dirs.insert(0, os.path.join(self.dataset_dir, cycle_dir))

        for directory in search_dirs:
            if not os.path.isdir(directory):
                continue
            for candidate in candidates:
                path = os.path.join(directory, candidate)
                if os.path.exists(path):
                    return path
            for f in os.listdir(directory):
                if f.lower() in {c.lower() for c in candidates}:
                    return os.path.join(directory, f)

        raise FileNotFoundError(f"File not found: {filename}")

    def load_file(self, filename: str) -> pd.DataFrame:
        """Loads an individual .xpt file. Tries latin1 encoding on UnicodeDecodeError."""
        path = self.find_file(filename)
        try:
            df, _ = pyreadstat.read_xport(path)
        except UnicodeDecodeError:
            df, _ = pyreadstat.read_xport(path, encoding="latin1")
        return df

