    @staticmethod
    def _df_to_markdown(df):
        cols = list(df.columns)
        lines = ['| ' + ' | '.join(cols) + ' |',
                 '| ' + ' | '.join(['---'] * len(cols)) + ' |']
        for _, row in df.iterrows():
            cells = []
            for v in row:
                if isinstance(v, float) and not pd.isna(v):
                    cells.append(f'{v:.4e}' if (abs(v) < 0.001 and v != 0) else f'{v:.4f}')
                elif isinstance(v, float) and pd.isna(v):
                    cells.append('—')
                else:
                    cells.append(str(v))
            lines.append('| ' + ' | '.join(cells) + ' |')
        return '\n'.join(lines)

    @staticmethod
    def _apply_fdr(tables):
        """
        Applies Benjamini-Hochberg (BH) FDR correction within each analysis family, adding a Q_value column.
        Tables without an assigned family are grouped under 'other'.
        Skipped for 'descriptive' tables (no p-values).
        """
        from collections import defaultdict
        family_pvals = defaultdict(list)
        family_locs = defaultdict(list)

        for i, entry in enumerate(tables):
            family = entry.get('family', 'other')
            if family == 'descriptive':
                continue
            df = entry['df']
            if 'P_value' not in df.columns:
                continue
            for j, p in enumerate(df['P_value']):
                if pd.notna(p) and np.isfinite(p) and 0 <= p <= 1:
                    family_pvals[family].append(p)
                    family_locs[family].append((i, j))

        if not family_pvals:
            return

        # Initialization (add Q_value and Q_family columns to tables containing P_value)
        for entry in tables:
            if entry.get('family') == 'descriptive':
                continue
            if 'P_value' in entry['df'].columns:
                entry['df']['Q_value'] = np.nan
                entry['df']['Q_family'] = entry.get('family', 'other')

        # Apply BH correction by family
        for family, pvals in family_pvals.items():
            if not pvals:
                continue
            _, qvals, _, _ = multipletests(pvals, method='fdr_bh')
            for (i, j), q in zip(family_locs[family], qvals):
                tables[i]['df'].loc[tables[i]['df'].index[j], 'Q_value'] = q

    def save_outputs(self, output_dir='.', prefix='nhanes_results'):
        """Saves all gathered analysis tables to Excel (separated by sheets) and Markdown formats."""
        os.makedirs(output_dir, exist_ok=True)
        ts = datetime.now().strftime('%Y%m%d_%H%M')

        # FDR Correction (applied across families before saving)
        self._apply_fdr(self._tables)

        # --- Excel ---
        xlsx_path = os.path.join(output_dir, f"{prefix}_{ts}.xlsx")
        try:
            used_names = set()
            with pd.ExcelWriter(xlsx_path, engine='openpyxl') as writer:
                for entry in self._tables:
                    raw = re.sub(r'[\\/:*?\[\]]', '', entry['title'])[:31]
                    sheet = raw
                    counter = 1
                    while sheet in used_names:
                        sheet = f"{raw[:28]}_{counter}"
                        counter += 1
                    used_names.add(sheet)
                    entry['df'].to_excel(writer, sheet_name=sheet, index=False)
            logging.info(f"Excel saved: {xlsx_path}")
        except ModuleNotFoundError as e:
            logging.warning(
                f"Excel output skipped because a dependency is missing ({e}). "
                "Markdown output will still be saved.")

        # --- Markdown ---
        md_path = os.path.join(output_dir, f"{prefix}_{ts}.md")
        with open(md_path, 'w', encoding='utf-8') as f:
            f.write("# NHANES GGT/Bil Analysis Results\n\n")
            f.write(f"Generated: {datetime.now().strftime('%Y-%m-%d %H:%M')}\n\n")
            f.write("## FDR correction families\n\n")
            f.write("Q-values are computed within each family (BH method). "
                    "Families: primary, interaction, sensitivity, specificity, "
                    "exploratory, methodological. Descriptive tables are excluded.\n\n")
            for entry in self._tables:
                family = entry.get('family', 'other')
                f.write(f"## {entry['title']}\n\n")
                f.write(f"_FDR family: **{family}**_\n\n")
                f.write(self._df_to_markdown(entry['df']))
                f.write('\n\n')
        logging.info(f"Markdown saved: {md_path}")


