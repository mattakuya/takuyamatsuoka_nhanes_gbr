    def run_oxidative_comparison(self, m=5):
        """
        Compares GBR and GGT alone with established dietary/lifestyle oxidative balance scores (CDAI, OBS, NutrientL-OBS).

        Protocol:
          - Restricts sample to individuals with available OBS data (all 20 components including cotinine;
            roughly the subset of cycles I/J with DR1DRSTZ=1).
          - Standardizes exposures by observed SD to compare beta coefficients per 1 SD.
          - Because OBS includes BMI, cotinine, physical activity, and alcohol, overlapping covariates
            (is_ever_smoker, alcohol_drinks, sedentary_min, and BMI main effect) are excluded from the
            formula to prevent double-counting.
            NutrientL-OBS only includes dietary alcohol, so only alcohol_drinks is excluded.
          - Because CDAI is a pure nutrient index (sum of 6 z-scores), it has no overlap with lifestyle covariates,
            so all adjustments are kept.

        Family: methodological (FDR correction applied independently from specificity/primary analyses).
        """
        logging.info("Starting Oxidative comparison (CDAI / OBS / GGT ratio)...")
        base = self.df[self.df['obs'].notna()].copy()
        if len(base) < 100:
            logging.warning(
                f"Oxidative comparison skipped: OBS-available sample too small "
                f"(n={len(base)}). Cotinine data may be missing.")
            return
        logging.info(
            f"  Restricted sample (OBS-available): n={len(base)} "
            f"(of {len(self.df)} total preprocessed).")

        # Exposure configurations: (column, label, exclude_covs, include_bmi_term)
        markers = [
            ('log_ratio',    "GGT/Bil Ratio (per 1 SD)",
                [], True),
            ('log_ggt',      "GGT alone (per 1 SD)",
                [], True),
            ('cdai',         "CDAI [6-item] (per 1 SD)",
                [], True),
            ('obs_nutrient', "NutrientL-OBS [16 dietary] (per 1 SD)",
                ['alcohol_drinks'], True),
            ('obs',          "OBS [full 20-component] (per 1 SD)",
                ['alcohol_drinks', 'is_ever_smoker', 'sedentary_min'], False),
        ]

        results = []
        for exposure, label, excl_covs, inc_bmi in markers:
            if exposure not in base.columns:
                logging.warning(f"  {label}: column '{exposure}' missing; skipping.")
