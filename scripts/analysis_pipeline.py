"""
NHANES GGT/Bil Ratio and Depressive Symptoms (PHQ-9) Association Analysis Pipeline

Main Design:
- NHANES Complex Survey Design: Cluster-robust SE using SDMVPSU
  (Uses cycle-suffixed composite PSU/strata IDs when merging multiple cycles)
  Effective degrees of freedom = n_psu - n_strata (compliant with Taylor Series Linearization [TSL])
  * Note: Variance is estimated using PSUs only, which is slightly conservative compared to full TSL.
    For full TSL variance estimation, R survey / samplics packages are recommended (documented in Limitations).

- Missing Data: Invalid categories are set to NaN and handled via MICE imputation.
- Multiple Imputation: IterativeImputer (sample_posterior=True, configurable max_iter)
  WTMEC2YR is included as an auxiliary variable in the imputation model.
  When sample_posterior=True, early stopping is disabled and iterations run up to max_iter.
- Parallelization: Imputation loop is parallelized using joblib if n_jobs > 1.
- Pooling: Rubin's Rules + Barnard-Rubin (1999) complete degrees-of-freedom correction.
- Primary Model: PHQ-9 total score regressed via WLS (weighted, cluster-robust).
- Sensitivity Analyses: Binary logistic regression (PHQ-9 >= 10), and stratified exclusions of CVD/diabetes/antidepressants.
- Interaction Analyses: log(GBR) * BMI, log(GBR) * antidepressant use pooled separately.
- Specificity: GGT/Bil vs log(ALT) vs log(AST) (standardized comparison).
- Sex Stratification: Accounts for sex differences in GGT and bilirubin.
- Others: Age >= 18 filter, Hispanic sub-analysis, replication in normal liver enzyme cohort,
  and Weighted Descriptive Table 1 generated by log(GBR) quartiles.
- Multiple Testing Correction: Benjamini-Hochberg (FDR) applied within each analysis family.
  family in {primary, interaction, sensitivity, specificity, methodological, exploratory, descriptive}
- Race Dummy Coding: Excludes rows with missing race, fixing Non-Hispanic White as the reference group.
- Inflammation Adjustment: Primary models adjust for log(NLR) (derived from CBC, available for all cycles).
  CRP/hs-CRP is evaluated in sensitivity analyses restricted to cycles where hs-CRP was measured.
"""

