    def run_oxidative_ad_interaction(self, m=5):
        """
        Tests the AD * exposure interaction for each biomarker in the OBS-available sample.

        Purpose:
          The crude OBS comparison (run_oxidative_comparison) might capture spurious negative correlations,
          as antidepressant users often have lower OBS (higher BMI, smoking, alcohol, lower PA) alongside higher PHQ-9.
          We test the AD interaction size and compare effects at the non-AD baseline.

        Each exposure is SD-standardized, and the formula includes exp_c + is_ad + exp_c:is_ad.
          - exp_c: Effect per 1 SD in non-AD users (is_ad=0).
          - exp_c:is_ad: Excess effect in AD users (AD-specific contrast).

        Family: oxidative_interaction (independent family for FDR correction to preserve statistical power in other families).
        """
        logging.info("Starting Oxidative × AD-interaction comparison...")
        base = self.df[self.df['obs'].notna()].copy()
        if len(base) < 100:
            logging.warning(
                f"Oxidative AD-interaction skipped: n={len(base)} too small.")
            return

