    def run_race_interaction_analysis(self, m=5, exposure='log_ratio'):
        """
        Formal global heterogeneity test for exposure × race/ethnicity.

        This complements race-stratified analyses by pooling the interaction
        coefficient vector and its covariance matrix across imputations, then
        testing all non-reference race interaction terms jointly with a Wald
        chi-square test. NHWhite is the reference group by construction.
        """
        logging.info(f"Starting race/ethnicity interaction analysis [{exposure}] (M={m})...")
        mdf, num_vars = self._prepare_imputation_df(
            self.df, include_race=True, include_sex=True, exposure=exposure)
        exp_c = f"{exposure}_c"

        race_cols = [f"race_{c}" for c in self.RACE_ORDER[1:]
                     if f"race_{c}" in mdf.columns]
        interaction_terms = [f"{exp_c}:{r}" for r in race_cols]
        extract_params = [exp_c] + interaction_terms

        exp_mean = mdf[exposure].mean(skipna=True)
        bmi_mean = mdf['BMXBMI'].mean(skipna=True)
        covs_excl = set(self.dpq_cols_clean) | {exposure, 'BMXBMI', 'WTMEC2YR'}
        covs = [c for c in num_vars if c not in covs_excl]

        def _single_imp(seed_offset):
            imp = IterativeImputer(max_iter=self.imputer_max_iter,
                                   random_state=42 + seed_offset,
                                   sample_posterior=True)
            idf = pd.DataFrame(imp.fit_transform(mdf[num_vars]),
                               columns=num_vars, index=mdf.index)

            for dv in self.DESIGN_VARS:
                if dv in mdf.columns:
                    idf[dv] = mdf[dv].values

            phq_items = idf[self.dpq_cols_clean].clip(0, 3)
            idf['phq9_score'] = phq_items.sum(axis=1)
            idf[exp_c] = idf[exposure] - exp_mean
            idf['bmi_c'] = idf['BMXBMI'] - bmi_mean

            covs_formula = ' + '.join(covs) if covs else '1'
            interaction_formula = ' + '.join(interaction_terms)
            formula = (
                f"phq9_score ~ {exp_c} + bmi_c + {covs_formula}"
                f" + {interaction_formula}"
            )
            model = smf.wls(formula, data=idf, weights=idf['WTMEC2YR'])
            res = self._fit_with_cluster(model, idf)

            coef_var = {}
            for p in extract_params:
                if p in res.params.index:
                    coef_var[p] = (float(res.params[p]), float(res.bse[p]) ** 2)

            available_interactions = [t for t in interaction_terms
                                      if t in res.params.index]
            beta_vec = res.params.loc[available_interactions].astype(float).values
            cov_mat = res.cov_params().loc[
                available_interactions, available_interactions
            ].astype(float).values
            nu = self._strata_nu(idf)
            return coef_var, beta_vec, cov_mat, available_interactions, nu

        # Sequential or parallel execution
        if self.n_jobs != 1 and _HAS_JOBLIB:
            logging.info(f"  [race interaction] Running {m} imputations in parallel "
                         f"(n_jobs={self.n_jobs})")
            # Use process-based 'loky' backend instead of 'threading' to bypass the GIL and utilize multiple cores
            results_list = Parallel(n_jobs=self.n_jobs, backend='loky')(
                delayed(_single_imp)(i) for i in range(m)
            )
        else:
            results_list = []
            for i in range(m):
                logging.info(f"  [race interaction] Imputation {i+1}/{m}...")
                results_list.append(_single_imp(i))

        first_terms = results_list[0][3] if results_list else []
        if not first_terms:
            logging.warning("Race interaction analysis skipped: no interaction terms.")
            return

        collected = {p: {'coefs': [], 'variances': []} for p in extract_params}
        beta_vectors = []
        cov_mats = []
        nu_com_list = []

        for coef_var, beta_vec, cov_mat, terms, nu in results_list:
            if terms != first_terms:
                logging.warning("Race interaction term set differed across imputations; skipping global test.")
                return
            for p, (c, v) in coef_var.items():
                collected[p]['coefs'].append(c)
                collected[p]['variances'].append(v)
            beta_vectors.append(beta_vec)
            cov_mats.append(cov_mat)
            nu_com_list.append(nu)

        nu_com = float(np.nanmean(nu_com_list)) if nu_com_list else None
        pooled = {}
        for p, v in collected.items():
            if len(v['coefs']) < m:
                continue
            beta, se, pval, df_r = self._apply_rubins(
                v['coefs'], v['variances'], m, nu_com=nu_com)
            t_crit = stats.t.ppf(0.975, df_r) if np.isfinite(df_r) else 1.96
            pooled[p] = {
                'beta': beta, 'se': se, 'p': pval, 'df': df_r,
                'ci_lo': beta - t_crit * se, 'ci_hi': beta + t_crit * se,
            }

        term_labels = {exp_c: f"{exp_c} (NHWhite reference)"}
        for term in interaction_terms:
            label = term.replace(f"{exp_c}:race_", f"{exp_c} × ")
            term_labels[term] = label
        self._print_pooled_terms(
            f"RACE/ETHNICITY INTERACTION ANALYSIS [{exposure}]",
            len(mdf), pooled, term_labels, family='interaction')

        beta_arr = np.vstack(beta_vectors)
        theta_bar = beta_arr.mean(axis=0)
        u_bar = np.mean(np.stack(cov_mats, axis=0), axis=0)
        b_mat = np.cov(beta_arr, rowvar=False, ddof=1)
        total_cov = u_bar + (1 + 1 / m) * b_mat
        try:
            inv_cov = np.linalg.inv(total_cov)
        except np.linalg.LinAlgError:
            inv_cov = np.linalg.pinv(total_cov)
        wald_chi2 = float(theta_bar.T @ inv_cov @ theta_bar)
        df_global = int(len(first_terms))
        p_global = float(stats.chi2.sf(wald_chi2, df_global))

        global_df = pd.DataFrame([{
            'Test': f'Global {exp_c} × race/ethnicity interaction',
            'N': len(mdf),
            'Chi_square': wald_chi2,
            'df': df_global,
            'P_value': p_global,
            'Terms_tested': ', '.join(first_terms),
        }])
        print(f"\n### GLOBAL RACE/ETHNICITY INTERACTION TEST [{exposure}] ###")
        print(global_df.to_string(index=False, float_format=lambda x: f"{x:.4f}"))
        self._tables.append({
            'title': f"GLOBAL RACE/ETHNICITY INTERACTION TEST [{exposure}]",
            'family': 'interaction',
            'df': global_df,
        })

