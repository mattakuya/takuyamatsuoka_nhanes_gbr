    def run_descriptive_by_race(self):
        """Outputs weighted mean (SD) of GGT, bilirubin, and GBR across racial/ethnic groups.
        Visualizes baseline distributions to help interpret ethnic heterogeneity."""
        logging.info("Building descriptive table by race...")
        df = self.df.dropna(subset=['WTMEC2YR']).copy()
        # Use harmonized 'race' (integrating cycles E/F via RIDRETH1).
        df['race_label'] = df['race']
        races = ['NHWhite', 'NHBlack', 'MexAm', 'OtherHisp', 'NHAsian']

        continuous_vars = [
            ('LBXSGTSI',  'GGT (U/L)'),
            ('LBXSTB',    'Total bilirubin (mg/dL)'),
            ('log_ratio', 'log(GGT/Bil)'),
            ('log_ggt',   'log(GGT)'),
            ('log_bil',   'log(Bil)'),
            ('LBXSATSI',  'ALT (U/L)'),
            ('LBXSASSI',  'AST (U/L)'),
            ('BMXBMI',    'BMI'),
            ('RIDAGEYR',  'Age'),
        ]

        def wmean_sd(x, w):
            valid = x.notna() & w.notna()
            if not valid.any():
                return np.nan, np.nan
            xw, ww = x[valid].astype(float), w[valid].astype(float)
            mean = np.average(xw, weights=ww)
            var = np.average((xw - mean) ** 2, weights=ww)
            return mean, float(np.sqrt(var))

        rows = []
        n_row = {'Variable': 'N (unweighted)', 'Type': 'count'}
        for r in races:
            n_row[r] = str(int((df['race_label'] == r).sum()))
        rows.append(n_row)

        for var, label in continuous_vars:
            row = {'Variable': label, 'Type': 'mean (SD)'}
            for r in races:
                sub = df[df['race_label'] == r]
                if var not in df.columns or sub.empty:
                    row[r] = '—'
                    continue
                mean, sd = wmean_sd(sub[var], sub['WTMEC2YR'])
                row[r] = f"{mean:.2f} ({sd:.2f})" if np.isfinite(mean) else '—'
            rows.append(row)

        table_df = pd.DataFrame(rows)
        print("\n### DESCRIPTIVE TABLE BY RACE (weighted) ###")
        print(table_df.to_string(index=False))
        self._tables.append({
            'title': 'DESCRIPTIVE TABLE BY RACE',
            'family': 'descriptive',
            'df': table_df,
        })

    def run_race_power_analysis(self):
        """Calculates the Minimum Detectable Effect (MDE) beta at 80% power from observed SEs across race strata.
        Also calculates post-hoc power assuming the observed beta in Non-Hispanic White is the true effect.
        For quick runs, uses a single WLS fit without MICE to approximate SE."""
        logging.info("Starting race-stratified power analysis...")
        from scipy.stats import norm

        race_groups = {
            'NHWhite':  self.df[self.df['race'] == 'NHWhite'],
            'NHBlack':  self.df[self.df['race'] == 'NHBlack'],
            'Hispanic': self.df[self.df['race'].isin(['MexAm', 'OtherHisp'])],
            'MexAm':    self.df[self.df['race'] == 'MexAm'],
            'OtherHisp':self.df[self.df['race'] == 'OtherHisp'],
            'NHAsian (G-J)': self.df[self.df['race'] == 'NHAsian'],
        }

        # Single imputation + cluster-robust WLS to estimate SE (quick approximation)
        ref_beta = None  # Use NHWhite beta as the reference effect size
        rows = []
        for race_label, subset in race_groups.items():
            n = len(subset)
            if n < 100:
                rows.append({'Race': race_label, 'N': n, 'Observed_beta': np.nan,
                              'SE': np.nan, 'MDE_80%_power': np.nan,
                              'Power_to_detect_NHWhite_beta': np.nan})
                continue
            try:
                mdf, num_vars = self._prepare_imputation_df(
                    subset, include_race=False, include_sex=True,
                    exposure='log_ratio')
                imp = IterativeImputer(max_iter=5, random_state=42,
                                       sample_posterior=False)
                idf = pd.DataFrame(imp.fit_transform(mdf[num_vars]),
                                   columns=num_vars, index=mdf.index)
                for dv in self.DESIGN_VARS:
                    if dv in mdf.columns:
                        idf[dv] = mdf[dv].values
                idf['phq9_score'] = idf[self.dpq_cols_clean].clip(0, 3).sum(axis=1)
                idf['log_ratio_c'] = idf['log_ratio'] - idf['log_ratio'].mean()
                idf['bmi_c']       = idf['BMXBMI'] - idf['BMXBMI'].mean()
                covs_excl = (set(self.dpq_cols_clean)
                             | {'log_ratio', 'BMXBMI', 'WTMEC2YR'})
                covs = [c for c in num_vars if c not in covs_excl]
                covs_formula = ' + '.join(covs) if covs else '1'
                formula = f"phq9_score ~ log_ratio_c + bmi_c + {covs_formula}"
                res = self._fit_with_cluster(
                    smf.wls(formula, data=idf, weights=idf['WTMEC2YR']), idf)
                beta = float(res.params.get('log_ratio_c', np.nan))
                se   = float(res.bse.get('log_ratio_c', np.nan))
            except Exception as e:
                logging.warning(f"  {race_label}: power calc failed ({e})")
                beta, se = np.nan, np.nan

            if race_label == 'NHWhite' and np.isfinite(beta):
                ref_beta = beta

            # Minimum Detectable Effect at 80% power (z_alpha/2 + z_beta) * SE = 2.802 * SE
            mde = 2.802 * se if np.isfinite(se) else np.nan
            # Post-hoc power assuming Non-Hispanic White beta as the true effect
            if ref_beta is not None and np.isfinite(se) and se > 0:
                ncp = abs(ref_beta) / se
                power = float(norm.cdf(ncp - 1.96) + norm.cdf(-ncp - 1.96))
            else:
                power = np.nan

            rows.append({
                'Race': race_label, 'N': n,
                'Observed_beta': beta, 'SE': se,
                'MDE_80%_power': mde,
                'Power_to_detect_NHWhite_beta': power,
            })

        # Re-loop to handle cases where NHWhite is not at the end
        for row in rows:
            if not np.isfinite(row.get('Power_to_detect_NHWhite_beta', np.nan)):
                se = row['SE']
                if ref_beta is not None and np.isfinite(se) and se > 0:
                    ncp = abs(ref_beta) / se
                    row['Power_to_detect_NHWhite_beta'] = float(
                        norm.cdf(ncp - 1.96) + norm.cdf(-ncp - 1.96))

        table_df = pd.DataFrame(rows)
        TITLE = "RACE-STRATIFIED POWER ANALYSIS"
        print(f"\n### {TITLE} ###")
        print(f"NHWhite reference β = "
              f"{ref_beta if ref_beta is not None else 'N/A'}")
        print(table_df.to_string(index=False,
                                  float_format=lambda x: f"{x:.4f}"))
        self._tables.append({
            'title': TITLE, 'family': 'methodological', 'df': table_df,
        })

    def run_ggt_bilirubin_incremental_analysis(self, m=5):
        """
        Three-part test of whether bilirubin adds information beyond GGT alone.

        1) Ratio-only vs GGT-only association, SD standardized.
        2) Joint model: log_GGT + log_bilirubin, testing bilirubin beyond GGT.
        3) Model fit comparison: ratio-only, GGT-only, and GGT+bilirubin joint
           models using mean AIC/BIC/adjusted R2/RMSE across imputations.

        This is not a formal causal superiority test. It asks whether the
        bilirubin component contributes independent inverse information and
        whether the ratio model performs at least as well as GGT alone.
        """
        logging.info("Starting GGT/Bilirubin incremental-value analysis...")

        # Fixed observed SD scaling so coefficients are comparable.
        df_scaled = self.df.copy()
        for col in ['log_ratio', 'log_ggt', 'log_bil']:
            sd = float(df_scaled[col].std(skipna=True))
            if sd and np.isfinite(sd) and sd > 0:
                df_scaled[col] = df_scaled[col] / sd

        # Part 1: ratio-only and GGT-only models.
        ratio = self._run_mice_core(
            df_scaled, m, "Ratio only: log(GGT/Bil) per 1 SD",
            exposure='log_ratio',
            standardize_exposure=False,
            include_bmi_interaction=False,
        )
        ggt = self._run_mice_core(
            df_scaled, m, "GGT only: log(GGT) per 1 SD",
            exposure='log_ggt',
            standardize_exposure=False,
            include_bmi_interaction=False,
        )
        self._print_results(
            "GGT/BIL INCREMENTAL ANALYSIS: RATIO vs GGT",
            [ratio, ggt],
            family='specificity',
        )

        # Part 2: bilirubin beyond GGT in a joint model.
        joint = self._run_mice_core(
            df_scaled, m, "Joint: log(GGT) + log(Bilirubin)",
            exposure='log_ggt',
            standardize_exposure=False,
            include_bmi_interaction=False,
            include_vars=['log_bil'],
            extract_params=['log_ggt_c', 'log_bil'],
        )
        self._print_pooled_terms(
            "BILIRUBIN BEYOND GGT (JOINT MODEL, per 1 SD)",
            joint['n'],
            joint['pooled'],
            {
                'log_ggt_c': 'log(GGT), adjusted for bilirubin',
                'log_bil':   'log(Bilirubin), adjusted for GGT',
            },
            family='specificity',
        )

        # Part 3: mean model fit metrics across imputations.
        rows = self._compare_ratio_ggt_fit_stats(df_scaled, m)
        if rows:
            fit_df = pd.DataFrame(rows)
            title = "MODEL FIT COMPARISON: RATIO vs GGT vs GGT+BIL"
            print(f"\n### {title} ###")
            print(fit_df.to_string(index=False))
            self._tables.append({
                'title': title,
                'family': 'methodological',
                'df': fit_df,
            })

