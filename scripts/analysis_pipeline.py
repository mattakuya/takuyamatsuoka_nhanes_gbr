    # =====================================================
    # CCA vs MICE Bias Verification
    # =====================================================
    def _run_cca_single(self, df_subset, exposure, sd_ref=None):
        """
        CCA: Drops missing rows and fits WLS directly (no imputation).
        If sd_ref is specified, standardizes exposure using the common observed SD.
        """
        mdf, num_vars = self._prepare_imputation_df(df_subset, include_race=True,
                                                      include_sex=True,
                                                      exposure=exposure)

        sd = sd_ref if (sd_ref is not None and sd_ref > 0) else mdf[exposure].std(skipna=True)
        if sd and sd > 0:
            mdf = mdf.copy()
            mdf[exposure] = mdf[exposure] / sd

        # Restrict to complete cases where all non-design variables are observed
        impute_cols = [c for c in num_vars if c not in self.DESIGN_VARS]
        cca_df = mdf.dropna(subset=impute_cols).copy()

        if len(cca_df) < 50:
            logging.warning(f"CCA [{exposure}]: complete cases too few ({len(cca_df)}), skipping.")
            return None

        exp_c = f"{exposure}_c"
        cca_df[exp_c]   = cca_df[exposure] - cca_df[exposure].mean()
        cca_df['bmi_c'] = cca_df['BMXBMI'] - cca_df['BMXBMI'].mean()

        # PHQ-9 total (no missingness as this is complete-case analysis)
        cca_df['phq9_score'] = cca_df[self.dpq_cols_clean].clip(0, 3).sum(axis=1)

        covs_excl = set(self.dpq_cols_clean) | {exposure, 'BMXBMI', 'WTMEC2YR'}
        covs = [c for c in num_vars if c not in covs_excl]
        covs_formula = ' + '.join(covs) if covs else '1'
        formula = f"phq9_score ~ {exp_c} + bmi_c + {covs_formula}"

        model = smf.wls(formula, data=cca_df, weights=cca_df['WTMEC2YR'])
        res = self._fit_with_cluster(model, cca_df)

        if exp_c not in res.params.index:
            return None

        beta = float(res.params[exp_c])
        se   = float(res.bse[exp_c])
        nu   = self._strata_nu(cca_df)
        df_r = nu if nu is not None else float(res.df_resid)
        t_crit = stats.t.ppf(0.975, df_r) if np.isfinite(df_r) else 1.96
        p = 2 * float(stats.t.sf(abs(beta / se), df=df_r)) if se > 0 else np.nan

        return {
            'method': 'CCA', 'n': len(cca_df),
            'beta': beta, 'se': se,
            'ci_lo': beta - t_crit * se, 'ci_hi': beta + t_crit * se,
            'p': p,
        }

